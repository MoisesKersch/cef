class Usuario {
    constructor(nome, email, cep, cidade, telefone, rua, latitude, longitude) {
        this.nome = nome;
        this.email = email;
        this.cep = cep;
        this.cidade = cidade;
        this.telefone = telefone;
        this.rua = rua;
        this.latitude = latitude;
        this.longitude = longitude;
    }
}
